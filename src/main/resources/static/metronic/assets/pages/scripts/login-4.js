	var getPhoneCode = function(){
	    var interValObj; //timer变量，控制时间
        var count = 60; //间隔函数，1秒执行
        var curCount;//当前剩余秒数
		$("#btnGetCode").click(function () {
			if($(this).hasClass("disabled")){
				return false;
			}
			curCount = count;
			interValObj = window.setInterval(setRemainTime, 1000);
			$(this).addClass("disabled");
			$.ajax({
                type: "POST",
                url: "sendPhonecode",
                data: {phone:$('#username').val()},
                dataType: 'json',
                success: function (json) {
                    if (!json.result) {
                    	alert(json.info);
						window.clearInterval(interValObj);
						$("#btnGetCode").removeClass("disabled");
						$("#btnGetCode").val("获取验证码");
                    }
                }
            });
		});
		
		function setRemainTime() {
	        if (curCount == 0) {
	            window.clearInterval(interValObj);//停止计时器
	            $("#btnGetCode").removeClass("disabled");
	            $("#btnGetCode").val("重送验证码");
	        }
	        else {
	            curCount--;
	            console.log(curCount)
	            $("#btnGetCode").val("输入验证码(" + curCount + ")");
	            //$("#phone").attr("readonly","readonly");
	        }
	    }
	}
	
	var userNameChange = function(){
		$('#username').on('input propertychange',function(){
			var phone = $(this).val();
			if(/^13[0-9]{9}$|14[0-9]{9}$|15[0-9]{9}$|17[0-9]{9}$|18[0-9]{9}$/.test(phone)){
				$("#btnGetCode").removeClass("disabled");
			}else{
				$("#btnGetCode").addClass("disabled");
			}
		});
	}
	
	
    
